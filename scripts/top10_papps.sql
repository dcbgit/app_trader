SELECT *
from play_store_apps
Where name = 'My Talking Tom' 
OR name = 'Temple Run 2' 
OR name = 'Geometry Dash Lite'
OR name = 'Jetpack Joyride'
OR name = 'Subway Surfers'
OR name = 'Angry Birds Rio'
OR name = 'Hill Climb Racing'
OR name = 'Candy Crush Saga'
OR name = 'Clash Royale'
OR name = 'Trivia Crack'
order by name;

